// Tipos de datos en TypeScript

// string: son de tipo texto
let texto:string = 'Soy de tipo texto';

// number: son númericos.
let numero:number = 12345;

// boolean: verdadero o falso
let verdadFalso:boolean = true;

// any: cualquier cosa
let cualquiera:any = "Cualquier cosa";

// string[]: Array de caracteres. number[]: Array de números. any[]: Array de cualquier cosa.
let arrayCaracter:string[] = ["nombre","apellido"];
let arrayNumero:number[];
let arrayBooleano:boolean[] = [true,false];
let arrayCualquiera:any[] = ["Texto",123,false];